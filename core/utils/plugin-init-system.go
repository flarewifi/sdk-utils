package tools

import (
	"core/utils/plugins"
	"fmt"
	"os"
	"path/filepath"

	sdkutils "github.com/flarehotspot/sdk-utils"
)

const (
	systemPluginInitAutoGeneratedComment = `/**
WARNING: This file is system-generated, do not edit nor commit into your repo.
Regenerated by core/cmd/sysplugin-prepare. The committed stub equivalent lives
at system-plugins-init.default — copy it to system-plugins-init.go if you need
a no-op version (e.g. before the generator has run for the first time).
*/
`
)

// CreateSystemPluginInit prepares the non-mono system-plugin integration:
// for every plugin under data/plugins/system/ it enforces the three-file plugin
// contract (//go:build !mono on main.go, main_mono.go generated if missing,
// system/main.go generated if missing — all driven from the plugin's root
// main.go), then writes the loader file core/internal/api/system-plugins-init.go
// which the non-mono boot calls into via PluginsMgr.LoadSystemPlugins.
//
// Devel/local/git/store plugins are intentionally NOT touched — they continue
// to ship as separately-built plugin.so files under non-mono. Only system
// plugins are statically linked into the core binary.
func CreateSystemPluginInit() {
	CreateGoWorkspace()

	systemDefs := plugins.SystemPluginSrcDefs()
	for _, def := range systemDefs {
		EnsureMainGoBuildTag(def.LocalPath)
		EnsurePluginMainMono(def.LocalPath)
		EnsurePluginSystemFiles(def.LocalPath)
		ValidatePluginContract(def.LocalPath)
	}

	MakeSystemPluginInit()
}

// MakeSystemPluginInit writes core/internal/api/system-plugins-init.go.
//
// When there are zero system plugins the output is a valid no-op stub matching
// system-plugins-init.default — this keeps the non-mono core package compilable
// regardless of whether any system plugins exist for the current product.
func MakeSystemPluginInit() {
	systemDefs := plugins.SystemPluginSrcDefs()

	pluginMods := []PluginModule{}
	seenPackages := make(map[string]bool)
	for _, def := range systemDefs {
		dir := def.LocalPath
		modVar := sdkutils.Slugify(filepath.Base(dir), "_")
		modPath := getGoModule(dir)
		pkgName := getPackage(dir)
		if seenPackages[pkgName] {
			continue
		}
		seenPackages[pkgName] = true
		pluginMods = append(pluginMods, PluginModule{
			PluginImportVar:   modVar,
			PluginModuleUri:   modPath,
			PluginPackageName: pkgName,
			DefSrc:            def.Src,
			DefLocalPath:      def.LocalPath,
		})
	}

	outPath := filepath.Join("core/internal/api/system-plugins-init.go")
	var newContent string

	if len(pluginMods) == 0 {
		// No system plugins for this product — emit a no-op LoadSystemPlugins so
		// the non-mono build still compiles. This matches the shape of
		// system-plugins-init.default.
		newContent = fmt.Sprintf(`//go:build !mono

%s

package api

func (self *PluginsMgr) LoadSystemPlugins(g *CoreGlobals) {}
`, systemPluginInitAutoGeneratedComment)
	} else {
		importModules := ""
		for _, mod := range pluginMods {
			// Same convention as plugin-init_mono.go: import the generated
			// /system subpackage so we have a non-`main` package to call into.
			importModules += fmt.Sprintf("\n\t"+`%s "%s/system"`, mod.PluginImportVar, mod.PluginModuleUri)
		}

		pluginInitCodes := ""
		for _, mod := range pluginMods {
			pkg := mod.PluginPackageName
			importVar := mod.PluginImportVar
			pluginInitCodes += fmt.Sprintf(`

	// Loading system plugin
	pkg = "%s"
	fmt.Println("Loading system plugin:" + pkg)

	pluginDir = filepath.Join(sdkutils.PathPluginInstallDir, pkg)
	info, err = sdkutils.GetPluginInfoFromPath(pluginDir)
	if err != nil {
		fmt.Println("Error getting plugin info from path: " + err.Error())
	} else {
		// Ensure plugin translations are available for current language (skip in dev mode)
		if env.GO_ENV != env.ENV_DEV {
			if err := sdkutils.EnsureTranslations(pluginDir, currentLang); err != nil {
				fmt.Println("Warning: Failed to ensure translations for plugin", pkg, ":", err)
			}
		}
		// Run migrations
		if err := migrate.MigrateUp(self.db.DB, pluginDir); err != nil {
			fmt.Println("Warning: failed to apply migrations for plugin :", pkg)
		}
		// Load plugin
		api = NewPluginApi(pluginDir, info, g.GlobalAssets, self, self.trfkMgr, g.WifiMgr)
		// Standard signature: func Init(api sdkapi.IPluginApi) error
		if err := %s.Init(api); err != nil {
			fmt.Println("Error initializing plugin", pkg, ":", err)
		} else {
			api.Initialize(coreAPI)
			api.LoadAssetsManifest()
			self.plugins = append(self.plugins, api)
			fmt.Println("Loaded system plugin:" + pkg)
		}
	}

		`, pkg, importVar)
		}

		newContent = fmt.Sprintf(`//go:build !mono

%s

package api

import (
	"fmt"
	"path/filepath"

	"core/utils/config"
	"core/utils/env"
	"core/utils/migrate"
	sdkutils "github.com/flarehotspot/sdk-utils"
    %s
)

func (self *PluginsMgr) LoadSystemPlugins(g *CoreGlobals) {
	coreAPI := self.CoreAPI

	// Get current language for translations (only needed if not in dev mode)
	currentLang := "en"
	if env.GO_ENV != env.ENV_DEV {
		cfg, err := config.ReadApplicationConfig()
		if err == nil {
			currentLang = cfg.Lang
		}
	}

	var pkg, pluginDir string
	var api *PluginApi
	var info sdkutils.PluginInfo
	var err error

	%s
}
`, systemPluginInitAutoGeneratedComment, importModules, pluginInitCodes)
	}

	var existing string
	if b, err := os.ReadFile(outPath); err == nil {
		existing = string(b)
	}

	if newContent != existing {
		if err := os.WriteFile(outPath, []byte(newContent), 0644); err != nil {
			panic(err)
		}
	}

	fmt.Println(outPath, "has been created.")
}
