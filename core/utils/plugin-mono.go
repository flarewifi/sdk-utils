package tools

import (
	"fmt"
	"os"
	"path/filepath"
	"regexp"
	"strings"
)

// Four-file plugin entry-point contract. Authors own the two root files;
// the generator owns the two system/ files as a derived mirror.
//
//   <plugin>/main.go              — AUTHOR. package main, //go:build !mono,
//                                   contains func main(){} and Init(api) error.
//                                   Used directly as the .so build entry for
//                                   non-mono local/devel/git/store plugins.
//                                   Also the source of truth from which
//                                   system/main.go is mirrored.
//
//   <plugin>/main_mono.go         — AUTHOR (or auto-generated from main.go on
//                                   first run if missing). package main,
//                                   //go:build mono. Provides Init under
//                                   -tags=mono for plugin-root compilation.
//                                   Also the source of truth from which
//                                   system/main_mono.go is mirrored.
//
//   <plugin>/system/main.go       — GENERATED, always overwritten with
//                                   content-aware-skip. Mirror of main.go
//                                   with package renamed to system and
//                                   func main() stripped. //go:build !mono
//                                   PRESERVED so Go selects this file when
//                                   the importer is in non-mono code.
//
//   <plugin>/system/main_mono.go  — GENERATED, always overwritten with
//                                   content-aware-skip. Mirror of main_mono.go
//                                   with package renamed to system and
//                                   func main() stripped. //go:build mono
//                                   PRESERVED so Go selects this file when
//                                   the importer is in mono code.
//
// Both loaders (mono and non-mono system) import <module>/system. Go's build
// tag filtering picks system/main.go or system/main_mono.go based on the
// loader's own //go:build tag. This gives per-mode static-link behavior
// without any branching in the generator or the loader templates.
//
// "Always overwrite with content-aware-skip" means: each prep run compares
// the bytes the generator would write against the existing file. If
// identical, no write happens (no mtime change, no reflex trigger). If
// different (e.g. author edited main.go), the file is rewritten.
//
// Drift between root and system/ is impossible because the generator runs
// before every build and re-mirrors. Direct edits to system/ files are
// silently overwritten — author customization happens in main.go and
// main_mono.go, not in system/.

const (
	mainSystemAutoGeneratedComment = `/*
AUTO-GENERATED by sysplugin-prepare on every prep run.

Snapshot of the plugin's root main.go (or main_mono.go) at generation time:
  - package renamed to "system"
  - func main() stripped (illegal in a non-main package)
  - //go:build tag PRESERVED so this file is selected under the same build
    tag as its source

DO NOT EDIT. Always regenerated when the source differs (content-aware-skip).
Edit the corresponding root main.go or main_mono.go instead. Any direct
edits here will be silently overwritten on the next sysplugin-prepare run.

Plugins with helpers split across multiple root .go files (e.g. init.go) must
move those helpers into this system/ subpackage by hand — the generator only
snapshots main.go and main_mono.go.
*/
`

	mainMonoAutoGeneratedComment = `// AUTO-GENERATED by sysplugin-prepare on first build.
//
// Snapshot of the plugin's root main.go retagged for mono. Under -tags=mono,
// main.go (tagged !mono) is excluded; this file provides the package main
// slot so the plugin directory still compiles. The mono loader does not
// actually import this file — it imports <plugin>/system — so the body is
// only consumed by tools that build the plugin directory directly.
//
// Generated ONLY when missing. Edit freely; sysplugin-prepare will not
// overwrite. Delete to force regeneration on the next build.
`
)

var (
	packageMainReg = regexp.MustCompile(`(?m)^package\s+\w+`)
	buildTagReg    = regexp.MustCompile(`(?m)^\s*//\s*go:build\s+.+\r?\n`)
	mainFuncReg    = regexp.MustCompile(`(?m)^func\s+main\s*\(\s*\)\s*\{[^}]*\}\s*`)
)

// EnsureMainGoBuildTag prepends //go:build !mono to <pluginDir>/main.go if no
// build tag is already present.
//
// The non-mono tag is required under the three-file contract so that
// main_mono.go can occupy the mono-tagged package main slot without a
// duplicate-symbol conflict. Idempotent: detects any existing //go:build tag
// (not just !mono) and no-ops, so authors who want a different tag
// combination can write their own first and the generator will respect it.
//
// Panics if main.go is missing — main.go is required by the contract.
func EnsureMainGoBuildTag(pluginDir string) {
	mainPath := filepath.Join(pluginDir, "main.go")
	data, err := os.ReadFile(mainPath)
	if err != nil {
		if os.IsNotExist(err) {
			panic(fmt.Errorf("EnsureMainGoBuildTag: %s missing (required by plugin contract)", mainPath))
		}
		panic(fmt.Errorf("EnsureMainGoBuildTag: unable to read %s: %w", mainPath, err))
	}

	if buildTagReg.Match(data) {
		return
	}

	content := "//go:build !mono\n\n" + string(data)
	if err := os.WriteFile(mainPath, []byte(content), 0644); err != nil {
		panic(fmt.Errorf("EnsureMainGoBuildTag: unable to write %s: %w", mainPath, err))
	}
	fmt.Printf("%s: prepended //go:build !mono\n", mainPath)
}

// EnsurePluginMainMono creates <pluginDir>/main_mono.go by snapshotting
// main.go and swapping the build tag to //go:build mono.
//
// Skipped if main_mono.go already exists — the file is author-owned after
// first generation. Plugin authors may freely customize (e.g. no-op under
// mono if the plugin should be excluded from mono builds) and the generator
// will not touch it again. Delete to force regeneration.
func EnsurePluginMainMono(pluginDir string) {
	destPath := filepath.Join(pluginDir, "main_mono.go")
	if fileExists(destPath) {
		fmt.Printf("%s exists — skipping (author-owned)\n", destPath)
		return
	}

	srcData := readMainGo(pluginDir, "EnsurePluginMainMono")

	content := string(srcData)
	content = buildTagReg.ReplaceAllString(content, "")
	content = strings.TrimLeft(content, "\n")
	out := mainMonoAutoGeneratedComment + "\n//go:build mono\n\n" + content

	if err := os.WriteFile(destPath, []byte(out), 0644); err != nil {
		panic(fmt.Errorf("EnsurePluginMainMono: unable to write %s: %w", destPath, err))
	}
	fmt.Printf("%s generated\n", destPath)
}

// EnsurePluginSystemFiles produces TWO files under <pluginDir>/system/:
//   - system/main.go      — mirror of root main.go      (//go:build !mono preserved)
//   - system/main_mono.go — mirror of root main_mono.go (//go:build mono  preserved)
//
// Both files have `package main` rewritten to `package system` and `func main()`
// stripped. Build tags are intentionally PRESERVED so the loader can import
// <module>/system and let Go's build-tag filtering pick the right file based
// on the loader's own //go:build tag (mono vs !mono).
//
// Always-regenerate semantics with content-aware-skip: each file is rewritten
// only if its current bytes differ from what the generator would produce.
// This means:
//   - Edits to root main.go / main_mono.go propagate automatically on next prep
//   - Direct edits to system/ files are silently overwritten (do not edit them)
//   - Reflex does not rebuild-loop because no-op runs do not touch mtimes
//
// Plugins with helpers split across multiple root .go files (e.g. publisher's
// init.go / init_mono.go) must move those helpers into system/ themselves —
// this generator only snapshots main.go and main_mono.go.
//
// Precondition: <pluginDir>/main_mono.go must exist by the time this runs.
// EnsurePluginMainMono (which creates it from main.go if missing) is called
// earlier in the standard prep sequence, so this holds in practice.
func EnsurePluginSystemFiles(pluginDir string) {
	systemDir := filepath.Join(pluginDir, "system")
	if err := os.MkdirAll(systemDir, 0755); err != nil {
		panic(fmt.Errorf("EnsurePluginSystemFiles: unable to create %s: %w", systemDir, err))
	}

	writePluginSystemMirror(pluginDir, "main.go")
	writePluginSystemMirror(pluginDir, "main_mono.go")
}

// ValidatePluginContract panics if any of the four required files is
// missing. Call after the Ensure* pass so misconfigured plugins fail at
// generation time rather than at compile time with a confusing error.
//
// The contract:
//   - main.go              (author, !mono, package main)
//   - main_mono.go         (author or auto-gen-from-main, mono, package main)
//   - system/main.go       (generated mirror of main.go, !mono preserved, package system)
//   - system/main_mono.go  (generated mirror of main_mono.go, mono preserved, package system)
func ValidatePluginContract(pluginDir string) {
	required := []string{
		"main.go",
		"main_mono.go",
		filepath.Join("system", "main.go"),
		filepath.Join("system", "main_mono.go"),
	}
	for _, rel := range required {
		full := filepath.Join(pluginDir, rel)
		if !fileExists(full) {
			panic(fmt.Errorf("plugin contract violation: %s is missing (required by four-file contract)", full))
		}
	}
}

// =============================================================================
// HELPER FUNCTIONS (internal)
// =============================================================================

func fileExists(path string) bool {
	_, err := os.Stat(path)
	return err == nil
}

func readMainGo(pluginDir, caller string) []byte {
	srcPath := filepath.Join(pluginDir, "main.go")
	data, err := os.ReadFile(srcPath)
	if err != nil {
		panic(fmt.Errorf("%s: unable to read %s: %w", caller, srcPath, err))
	}
	return data
}

// writePluginSystemMirror snapshots <pluginDir>/<srcName> into
// <pluginDir>/system/<srcName> with the package renamed to system and any
// func main() stripped. The //go:build tag on the source is intentionally
// preserved — that is what lets the loader dispatch between the mono and
// non-mono variants via Go's standard build-tag filtering.
//
// Content-aware-skip: if the destination already contains the exact bytes the
// generator would produce, the file is left untouched (no write, no mtime
// change). This prevents reflex rebuild loops in steady state.
func writePluginSystemMirror(pluginDir, srcName string) {
	srcPath := filepath.Join(pluginDir, srcName)
	destPath := filepath.Join(pluginDir, "system", srcName)

	srcData, err := os.ReadFile(srcPath)
	if err != nil {
		panic(fmt.Errorf("writePluginSystemMirror: unable to read %s: %w", srcPath, err))
	}

	content := string(srcData)
	content = packageMainReg.ReplaceAllString(content, "package system")
	content = mainFuncReg.ReplaceAllString(content, "")
	// NOTE: build tag is NOT stripped — it's preserved so the file is
	// selected only under the matching tag (mono or !mono), giving the
	// loaders natural per-mode dispatch.

	out := mainSystemAutoGeneratedComment + "\n" + content

	if existing, err := os.ReadFile(destPath); err == nil && string(existing) == out {
		return
	}

	if err := os.WriteFile(destPath, []byte(out), 0644); err != nil {
		panic(fmt.Errorf("writePluginSystemMirror: unable to write %s: %w", destPath, err))
	}
	fmt.Printf("%s regenerated\n", destPath)
}
